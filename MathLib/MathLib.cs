using System;

namespace DNP
{
    public class MathLib
    {



        public int int_addition(int a, int b)
        {
            return a + b;
        }
        public int int_subtraction(int a, int b)
        {
            return a - b;
        }
        public int int_multiplication(int a, int b)
        {
            return a * b;
        }
        public int int_division(int a, int b)
        {
            return a / b;
        }

        public double double_addition(double a, double b)
        {
            return a + b;
        }
        public double double_subtraction(double a, double b)
        {
            return a - b;
        }
        public double double_multiplication(double a, double b)
        {
            return a * b;
        }
        public double double_division(double a, double b)
        {
            return a / b;
        }


    }
}
